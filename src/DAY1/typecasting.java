package DAY1;

public class typecasting
{
    public static void main(String[] args)
    {
        int a=130;
        System.out.println(a);
        double d=a;
        System.out.println(d);
        int b=(int)d;
        System.out.println(b);

        int s=130;
        byte t=(byte)s;
        System.out.println(t);
        char std='A';
        int c=(int)std;
        System.out.println(c);
    }
}
